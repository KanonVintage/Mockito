public class operaciones {
	//suma de dos numeros
	public int sum (int a,int b) {
		return a + b;
	}
	public int mul (int a,int b) {
		return a * b;
	}
	public int div (int a,int b) {
		return a / b;
	}
	public int res (int a,int b) {
		return a - b;
	}
	
	public operaciones(){
		int a=0;
		int b=0;
	}
}
